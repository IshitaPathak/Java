public class Primitives{
    public static void main(String[] args){
        // primitive is any datatype that you cant break further
        int rollno=43;
        char letter='r'; // for char we use single quote
        float marks=3432.34f;
        double largeDecimalNumbers=343243243.34234;
        long largeIntegerValues = 342343244254578L;
        boolean check=false;

        // string is not primitive
        String name="Kunal"; //for string we use double quote
    }
}